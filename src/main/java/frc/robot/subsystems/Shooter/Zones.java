package frc.robot.subsystems.Shooter;

/**
   * The color of zones for shooting during Autonomous
   */
public enum Zones {      // Rows on field
    GREEN,               // 0 - 3 
    YELLOW,              // 3 - 5
    BLUE,                // 5 - 7
    RED,                 // 7 - 9
    REINTRODUCTION_ZONE, // 9 - End
    PARTY,               // Fun
    GREEN_FLAT,          // 0 - 3, flat target
    YELLOW_FLAT,         // 3 - 5, flat target
    BLUE_FLAT,           // 5 - 7, flat target
    RED_FLAT,            // 7 - 9, flat target
    POWER_YELLOW,        // For minute run
    SNOWBALL_THROWER     // For state competition
}
